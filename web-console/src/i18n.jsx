const I18nContext = createContext(null);

function normalizeLanguage(rawValue) {
  const text = String(rawValue || "").trim().toLowerCase();
  if (text.startsWith("zh")) {
    return "zh-CN";
  }
  return "en";
}

function detectPreferredLanguage() {
  const sources = Array.isArray(window.navigator.languages) && window.navigator.languages.length
    ? window.navigator.languages
    : [window.navigator.language || "en"];
  return normalizeLanguage(sources[0] || "en");
}

function resolvePath(source, key) {
  return String(key)
    .split(".")
    .reduce((current, part) => (current && Object.prototype.hasOwnProperty.call(current, part) ? current[part] : undefined), source);
}

function interpolate(template, vars) {
  if (!vars || typeof template !== "string") {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_match, key) => {
    if (!Object.prototype.hasOwnProperty.call(vars, key)) {
      return `{${key}}`;
    }
    return String(vars[key]);
  });
}

