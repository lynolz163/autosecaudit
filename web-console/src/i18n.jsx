export function I18nProvider({ children }) {
  const [language, setLanguageState] = useState(() => {
    const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return normalizeLanguage(stored || detectPreferredLanguage());
  });

  function t(key, vars = null) {
    const languageTable = translations[language] || translations.en;
    const fallbackTable = translations.en;
    const resolved = resolvePath(languageTable, key);
    const fallback = resolvePath(fallbackTable, key);
    const template = typeof resolved === "string" ? resolved : typeof fallback === "string" ? fallback : key;
    return interpolate(template, vars);
  }

  function setLanguage(nextLanguage) {
    const normalized = normalizeLanguage(nextLanguage);
    if (!SUPPORTED_LANGUAGES.includes(normalized)) {
      return;
    }
    setLanguageState(normalized);
  }

  function formatStatus(value) {
    const key = String(value || "unknown").trim().toLowerCase() || "unknown";
    return t(`status.${key}`);
  }

  function formatRole(value) {
    const key = String(value || "viewer").trim().toLowerCase() || "viewer";
    const translated = resolvePath(translations[language] || translations.en, `role.${key}`);
    return typeof translated === "string" ? translated : value || t("common.unknown");
  }

  function formatMode(value) {
    const key = String(value || "agent").trim().toLowerCase() || "agent";
    const translated = resolvePath(translations[language] || translations.en, `mode.${key}`);
    return typeof translated === "string" ? translated : value || t("common.unknown");
  }

  function formatBoolean(value) {
    return value ? t("common.yes") : t("common.no");
  }

  function formatPluginCategory(value) {
    const key = String(value || "").trim().toLowerCase();
    const translated = resolvePath(translations[language] || translations.en, `pluginCategory.${key}`);
    return typeof translated === "string" ? translated : value || t("common.unknown");
  }

  function formatExportFormat(value) {
    const key = String(value || "").trim().toLowerCase();
    const translated = resolvePath(translations[language] || translations.en, `exportFormat.${key}`);
    return typeof translated === "string" ? translated : value || t("common.unknown");
  }

  function localizeMessage(message) {
    const raw = String(message || "").trim();
    if (!raw) {
      return "";
    }
    const mappedKey = mapErrorKey(raw);
    if (mappedKey) {
      return t(mappedKey);
    }
    if (raw.startsWith("HTTP ")) {
      return t("errors.httpFallback", { value: raw });
    }
    return raw;
  }

  useEffect(() => {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = language === "zh-CN" ? "zh-CN" : "en";
    document.title = t("documentTitle");
  }, [language]);

  return (
    <I18nContext.Provider
      value={{
        language,
        setLanguage,
        t,
        formatStatus,
        formatRole,
        formatMode,
        formatBoolean,
        formatPluginCategory,
        formatExportFormat,
        localizeMessage,
      }}
    >
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}
